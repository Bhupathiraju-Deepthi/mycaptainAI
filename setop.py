e={0,1,2,3,4}
n={4,5,6,8,2}
# set union operation
print("union of e and n:",e|n)
# set intersection operation
print("intersection of e and n:",e&n)
# set difference operation
e={0,8,6,1,2,3,4,5}
n={1,2,3,4,5}
print("difference of e and n:",e-n)
# set symmetric difference operation
e={0,1,2,3,4}
n={4,5,6,8,2}
print("symmetric difference of e and n:",e^n)
